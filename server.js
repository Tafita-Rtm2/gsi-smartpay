// GSI SmartPay - Custom server.js for cPanel/Node.js App
// This file goes to the root of your application in cPanel
const path = require('path');
require('dotenv').config({ path: path.join(__dirname, '.env') });

const { createServer } = require('http');
const { parse } = require('url');
const next = require('next');

const dev = process.env.NODE_ENV !== 'production';
const hostname = 'localhost';
const port = process.env.PORT || 3000;

// On charge l'application Next.js
const app = next({ dev, hostname, port });
const handle = app.getRequestHandler();

app.prepare().then(() => {
  createServer(async (req, res) => {
    try {
      const parsedUrl = parse(req.url, true);
      const { pathname } = parsedUrl;

      // Log pour debug si besoin (sera dans stderr/stdout de cPanel)
      // console.log(`[Request]: ${req.method} ${pathname}`);

      // On laisse Next.js tout gérer
      await handle(req, res, parsedUrl);
    } catch (err) {
      console.error('Error occurred handling', req.url, err);
      res.statusCode = 500;
      res.end('Internal Server Error');
    }
  })
    .once('error', (err) => {
      console.error(err);
      process.exit(1);
    })
    .listen(port, () => {
      console.log(`> Ready on http://${hostname}:${port}`);
    });
});
